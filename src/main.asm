; ================================================================
; Файл: main.asm
; Описание: Точка входа, векторы и основной цикл
; ================================================================

$INCLUDE (defs.inc)

; Объявление глобальных меток для других модулей
PUBLIC Main

; Объявление внешних меток (из других файлов)
EXTRN CODE (Init_All)
EXTRN CODE (UART_ISR)
EXTRN CODE (Process_Command)
EXTRN DATA (FLAGS)

; ================================================================
; Таблица векторов прерываний
; ================================================================
CSEG AT 0000H
    JMP Main        ; Вектор сброса

CSEG AT 0023H
    JMP UART_ISR    ; Вектор прерывания UART

; ================================================================
; Код основной программы
; ================================================================
CSEG AT 0030H

Main:
    ; 1. Инициализация указателя стека
    MOV SP, #60H

    ; 2. Настройка периферии (вызов подпрограммы из init.asm)
    CALL Init_All

    ; 3. Разрешение прерываний
    SETB EA         ; Глобальное разрешение прерываний

Main_Loop:
    ; Бесконечный цикл опроса флага готовности пакета
    ; Флаг устанавливается в прерывании (isr_uart.asm)

    MOV A, FLAGS
    JNB ACC.0, Main_Loop ; Если бит 0 = 0, ждем дальше

    ; --- Пакет принят ---
    CLR EA               ; Запрет прерываний на время обработки

    CALL Process_Command ; Вызов обработчика команд

    ; Сброс флага "Пакет принят"
    MOV A, FLAGS
    ANL A, #11111110B
    MOV FLAGS, A

    SETB EA              ; Разрешение прерываний

    JMP Main_Loop

END
