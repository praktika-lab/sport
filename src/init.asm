; ================================================================
; Файл: init.asm
; Описание: Настройка оборудования (UART, Таймеры)
; ================================================================

$INCLUDE (defs.inc)

PUBLIC Init_All

CSEG

Init_All:
    ; --- 1. Настройка UART (Режим 1, 8 бит, REN включен) ---
    MOV SCON, #50H

    ; --- 2. Настройка Таймера 2 (Baud Rate Generator) ---
    ; Обеспечивает точные 9600 бод при 11.0592 МГц
    MOV T2CON, #34H     ; RCLK=1, TCLK=1, TR2=1
    MOV RCAP2H, #BAUD_H
    MOV RCAP2L, #BAUD_L

    ; --- 3. Настройка Таймера 0 (Для измерения длительности) ---
    ; Режим 1 (16 бит), используется в команде 0x09
    MOV TMOD, #01H

    ; --- 4. Настройка масок прерываний ---
    SETB ES             ; Разрешить прерывание UART

    ; --- 5. Сброс переменных ---
    MOV RX_INDEX, #0
    MOV FLAGS, #0

    RET
END
