-- ЗАДАНИЕ 2: Динамический PIVOT
--
-- ClickHouse не имеет встроенного PIVOT, поэтому используем два подхода:
--
-- ВАРИАНТ А (статический SQL — для известного набора значений):
--   sumIf(w, <pivot_col> = <value>) AS col_<value>
--
-- ВАРИАНТ Б (динамический — генерируется из Python в DAG):
--   Python читает уникальные значения pivot_col и строит SQL на лету
--
-- Ниже — шаблон ВАРИАНТА А.
-- PIVOT_COL и значения подставляются Python-оператором в DAG.
-- =============================================================================

-- ============================================================
-- Пример: разворот по полю  flev  (значения: 0, 1, 2)
-- Остаются колонки: slev, dat1 + по одной колонке на каждое значение flev
-- ============================================================

SELECT
    slev,
    dat1,
    sumIf(w, flev = 0)  AS flev_0,
    sumIf(w, flev = 1)  AS flev_1,
    sumIf(w, flev = 2)  AS flev_2
FROM pivot_db.rev_cross_tab
GROUP BY slev, dat1
ORDER BY slev, dat1;

